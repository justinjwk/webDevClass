package kim_module10;

/**
 *
 * @author Justin
 */
public class Success {
    
    private String hike;
    private BookingDay bookingDay;
    private int duration;
    private int numOfPeople;
    private double cost;

    /**
     * @return the hike
     */
    public String getHike() {
        return hike;
    }

    /**
     * @param hike the hike to set
     */
    public void setHike(String hike) {
        this.hike = hike;
    }

    /**
     * @return the bookingDay
     */
    public BookingDay getBookingDay() {
        return bookingDay;
    }

    /**
     * @param bookingDay the bookingDay to set
     */
    public void setBookingDay(BookingDay bookingDay) {
        this.bookingDay = bookingDay;
    }

    /**
     * @return the duration
     */
    public int getDuration() {
        return duration;
    }

    /**
     * @param duration the duration to set
     */
    public void setDuration(int duration) {
        this.duration = duration;
    }

    /**
     * @return the numOfPeople
     */
    public int getNumOfPeople() {
        return numOfPeople;
    }

    /**
     * @param numOfPeople the numOfPeople to set
     */
    public void setNumOfPeople(int numOfPeople) {
        this.numOfPeople = numOfPeople;
    }

    /**
     * @return the cost
     */
    public double getCost() {
        return cost;
    }

    /**
     * @param cost the cost to set
     */
    public void setCost(double cost) {
        this.cost = cost;
    }

    
    
    
}
