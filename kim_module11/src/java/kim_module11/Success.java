package kim_module11;

/**
 *
 * @author Justin
 */
public class Success {
    
    private String date;
    private String location;
    private String guideName;
    private String reservationFirst;
    private String reservationLast;

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the location
     */
    public String getLocation() {
        return location;
    }

    /**
     * @param location the location to set
     */
    public void setLocation(String location) {
        this.location = location;
    }

    /**
     * @return the guideName
     */
    public String getGuideName() {
        return guideName;
    }

    /**
     * @param guideName the guideName to set
     */
    public void setGuideName(String guideName) {
        this.guideName = guideName;
    }

    /**
     * @return the reservationFirst
     */
    public String getReservationFirst() {
        return reservationFirst;
    }

    /**
     * @param reservationFirst the reservationFirst to set
     */
    public void setReservationFirst(String reservationFirst) {
        this.reservationFirst = reservationFirst;
    }

    /**
     * @return the reservationLast
     */
    public String getReservationLast() {
        return reservationLast;
    }

    /**
     * @param reservationLast the reservationLast to set
     */
    public void setReservationLast(String reservationLast) {
        this.reservationLast = reservationLast;
    }

}
